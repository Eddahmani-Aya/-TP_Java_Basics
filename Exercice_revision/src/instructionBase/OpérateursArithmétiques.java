package instructionBase;

public class OpérateursArithmétiques {
	public static void main(String[] args) {
		double n1,n2,resultat; 
		n1=7;
		n2=2;
		resultat=n1+n2;
		System.out.println(resultat);
		resultat=n1-n2;
		System.out.println(resultat);
		resultat=n1*n2;
		System.out.println(resultat);
		resultat=n1/n2;
		System.out.println(resultat);
		resultat=n1%n2;
		System.out.println(resultat);
	}
}
